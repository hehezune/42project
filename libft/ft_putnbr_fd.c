#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	number;

	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		number = -n;
		if (n == -2147483648)
		{
			ft_putnbr_fd(number / 10, fd);
			ft_putchar_fd(number % 10 + '0', fd);
		}
		else
			ft_putnbr_fd(number, fd);
	}
	else if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd(n % 10 + '0', fd);
	}
	else
		ft_putchar_fd(n + '0', fd);
}
