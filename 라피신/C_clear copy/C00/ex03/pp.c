#include	<unistd.h>

int	main(void)
{
	char c;
	c = 'a';
	write(1, &c, 1);
	write(1, "b", 1);
	

	if (c == 'a')
	{	
		write(1, &c, 1);
		c++;
	}
//	이거는 ㅇ이런의미	write(1, "\n", 1);
	while (c <= 'z')
	{
		c++;
		write(1,&c,1);
	}
}
